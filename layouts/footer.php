     document.addEventListener('DOMContentLoaded', function() {
         const scrollerBtn = document.querySelector('.scroller-btn');
         const scrollThreshold = 300;
         const bottomThreshold = 100;

         function updateScroller() {
             const scrollPosition = window.pageYOffset;
             const windowHeight = window.innerHeight;
             const documentHeight = document.body.scrollHeight;
             // Hide if at top or bottom
             if (scrollPosition < scrollThreshold &&
                 scrollPosition + windowHeight >= documentHeight - bottomThreshold) {
                 scrollerBtn.classList.remove('visible');
                 return;
             }
             // Determine direction
             if (scrollPosition + windowHeight >= documentHeight - bottomThreshold) {
                 scrollerBtn.classList.add('scroll-up');
                 scrollerBtn.classList.remove('scroll-down');
                 scrollerBtn.setAttribute('aria-label', 'Scroll to top');
                 scrollerBtn.setAttribute('title', 'Go to top');
             } else {
                 scrollerBtn.classList.add('scroll-down');
                 scrollerBtn.classList.remove('scroll-up');
                 scrollerBtn.setAttribute('aria-label', 'Scroll to bottom');
                 scrollerBtn.setAttribute('title', 'Go to bottom');
             }
             scrollerBtn.classList.add('visible');
         }

         function handleScroll() {
             window.scrollTo({
                 top: scrollerBtn.classList.contains('scroll-up') ? 0 : document.body.scrollHeight,
                 behavior: 'smooth'
             });
         }
         // Initial check
         updateScroller();
         // Listen for scroll events
         window.addEventListener('scroll', updateScroller);
         // Button click handler
         scrollerBtn.addEventListener('click', function(e) {
             e.preventDefault();
             handleScroll();
         });
     });
 </script>

